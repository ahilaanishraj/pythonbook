# Creating a dictionary
my_dict = {
    'name': 'Anu',
    'age': 30,
    'city': 'Nagarcoil'
}

# Accessing values using keys
name = my_dict['name']  # Access the 'name' key's value ('Anu')
age = my_dict['age']    # Access the 'age' key's value (30)
city = my_dict['city']  # Access the 'city' key's value ('Nagarcoil')

# Printing the values
print("Name:", name)
print("Age:", age)
print("City:", city)
